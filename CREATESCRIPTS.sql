CREATE TABLE COUNTRY(
COUNTRYID INTEGER NOT NULL,
COUNTRYNAME NVARCHAR2(100),
CONTINENTID INTEGER NOT NULL,

CONSTRAINT PK_COUNTRY PRIMARY KEY(COUNTRYID),
CONSTRAINT FK_CONTINENT_COUNTRY FOREIGN KEY (COUNTRYID) REFERENCES CONTINENT(ID)
);


CREATE TABLE CONTINENT(
ID INTEGER NOT NULL,
CONTINENTNAME NVARCHAR2(100),
COUNTRYID INTEGER NOT NULL,

CONSTRAINT PK_CONTINENT PRIMARY KEY(CONTINENTID)
);


CREATE TABLE STATE_(
ID INTEGER,
STATENAME NVARCHAR2(100),
COUNTRYID INTEGER,

CONSTRAINT PK_STATE PRIMARY KEY(ID),
CONSTRAINT FK_STATE FOREIGN KEY(COUNTRYID) REFERENCES COUNTRY(COUNTRYID)
);



CREATE TABLE CITY(
ID INTEGER,
CITYNAME NVARCHAR2(100),
STATEID INTEGER,

CONSTRAINT PK_CITY PRIMARY KEY,
CONSTRAINT FK_CITY_STATE FOREIGN KEY (STATEID) REFERENCES STATE_(ID)
);


CREATE TABLE AGERANGE(
ID INTEGER,
RANGE NVARCHAR2(50),
RANGENAME NVARCHAR2(50),

CONSTRAINT PK_AGERANGE PRIMARY KEY(ID)

);



CREATE TABLE GENDERAGERANGE(
ID INTEGER,
GENDER CHAR(1),
AGERANGEID INTEGER,
TOTALDEATHS INTEGER,
TOTALCASES INTEGER,

CONSTRAINT PK_GENDERAGERANGE PRIMARY KEY(ID),
CONSTRAINT FK_AGERANGEID FOREIGN KEY(AGERANGEID) REFERENCES AGERANGE(ID)
);



CREATE TABLE CASES(
CASEID INTEGER,
COUNTRYID INTEGER,
STATEID INTEGER,
CITYID INTEGER,
TOTALCASES NUMBER,
