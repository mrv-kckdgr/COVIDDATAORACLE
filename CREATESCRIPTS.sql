CONSTRAINT PK_CASEID PRIMARY KEY(CASEID),
CONSTRAINT FK_CASES_COUNTRY FOREIGN KEY(COUNTRYID) REFERENCES COUNTRY(COUNTRYID),
CONSTRAINT FK_CASES_STATE FOREIGN KEY(STATEID) REFERENCES STATE_(ID),
CONSTRAINT FK_CASES_CITY FOREIGN KEY(CITYID) REFERENCES CITY(ID)

